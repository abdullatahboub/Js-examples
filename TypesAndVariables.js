//Types : Primitive and Reference Types

//Primitive Types
//1)Numbers

var myNumber = 3;
console.log(myNumber);

//2)Strings

const myString = "I am a string type value!";
console.log(myString);

//3)NaN

let a = NaN;
console.log(a);

//4)undefined

let b = undefined;
